let LANG = 'tr';

export function initLang() {
  let saved = null;
  try { saved = localStorage.getItem('sahanaliz_lang'); } catch (e) {}
  if (saved && STR[saved]) { LANG = saved; return LANG; }
  let nav = 'tr';
  try { nav = (navigator.language || navigator.userLanguage || 'tr').slice(0, 2).toLowerCase(); } catch (e) {}
  LANG = STR[nav] ? nav : 'en';
  return LANG;
}

export function getLang() { return LANG; }

export function setLang(code) {
  if (!STR[code]) return;
  LANG = code;
  try { localStorage.setItem('sahanaliz_lang', code); } catch (e) {}
  applyI18n();
}

export function t(key, params) {
  let s = (STR[LANG] && STR[LANG][key]) || STR.en[key] || key;
  if (params) for (const k in params) s = s.replace('{' + k + '}', params[k]);
  return s;
}

// Replace text/placeholder/title on elements carrying data-i18n* attributes.
export function applyI18n(root) {
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.getAttribute('data-i18n')); });
  root.querySelectorAll('[data-i18n-html]').forEach((el) => { el.innerHTML = t(el.getAttribute('data-i18n-html')); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph'))); });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => { el.title = t(el.getAttribute('data-i18n-title')); });
  try { document.documentElement.lang = LANG; } catch (e) {}
}
